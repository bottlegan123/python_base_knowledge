# Python print
print("Dad"+"hello"+"!")
print("Dad"+" hello"+"!")  # 加号把字符串连在一起,空格也是一个字符串

# print("He said "hello"")  这里双引号出现混淆会报错
print('He said "hello"')  # 单双引号的使用
print("He said 'hello'")
print('He said "Let\'s go!"')  # 引号前面加/会产生特殊的意思
print("He said \"Let\'s go!\"")  # 颜色一样的反斜杠和引号是一伙的

# Python换行打印
print("Hi \nBob")  # \n表示换行输出
print("\n")

# python多行运行
print("hello\
 world")

# 三个引号几行执行而不是一行运行完了就结束
print('''Never send to know for whom the bell tolls,
It tolls for you.''')

print('Never send to know for whom the bell tolls, \nIt tolls to you.')


